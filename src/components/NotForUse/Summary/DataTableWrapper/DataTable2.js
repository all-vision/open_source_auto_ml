const screenWidth =
  window.innerWidth ||
  document.documentElement.clientWidth ||
  document.body.clientWidth;

const useStyles = makeStyles({
  root: {
    width: screenWidth < 1900 ? '65rem' : '95rem',
    marginTop: '2vh',
  },
  container: {
    maxHeight: screenWidth < 1900 ? 550 : 700,
    overflowY: 'scroll',
  },
});

export default function StickyHeadTable(props) {
  const classes = useStyles();
  const [page, setPage] = React.useState(0);
  const [rowsPerPage, setRowsPerPage] = React.useState(50);

  const handleChangePage = (event, newPage) => {
    setPage(newPage);
  };

  const handleChangeRowsPerPage = (event) => {
    if (event.target.value !== 'all') {
      setRowsPerPage(+event.target.value);
    } else {
      setRowsPerPage(+props.titanic_data.length);
    }
    setPage(0);
  };

  return (
    <Paper className={classes.root}>
      <TableContainer className={classes.container}>
        <Table stickyHeader aria-label="sticky table">
          <TableHead>
            <TableRow>
              {props.titanic_data[0].map((col, index) => {
                return (
                  <TableCell align="left" key={index}>
                    {col}
                  </TableCell>
                );
              })}
            </TableRow>
          </TableHead>
          <TableBody>
            {props.titanic_data.slice(1, rowsPerPage).map((col, index) => {
              return (
                <TableRow key={index}>
                  {col.map((c, index) => {
                    return (
                      <TableCell align="left" key={index}>
                        {c}
                      </TableCell>
                    );
                  })}
                </TableRow>
              );
            })}
          </TableBody>
        </Table>
      </TableContainer>
      <TablePagination
        rowsPerPageOptions={[50, 100, 200, 'all']}
        component="div"
        count={rows.length}
        rowsPerPage={rowsPerPage !== 'all' ? rowsPerPage : 'all'}
        page={page}
        onChangePage={handleChangePage}
        onChangeRowsPerPage={handleChangeRowsPerPage}
      />
    </Paper>
  );
}
